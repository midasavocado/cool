    let updateCount=()=>{};

    /* ─────────── NAVIGATION ─────────── */
    const clearActive=()=>slangifiedSpans.forEach(s=>s.style.outline='');
    function nav(step){ if(!highlightEnabled||!slangifiedSpans.length)return; navIndex=(navIndex+step+slangifiedSpans.length)%slangifiedSpans.length;
        clearActive(); const el=slangifiedSpans[navIndex]; if(el&&document.body.contains(el)){ el.style.outline='2px solid red'; el.scrollIntoView({behavior:'smooth',block:'center'}); } }

    /* ─────────── ARTICLE FIXER ─────────── */
    const vowelRegex=/^[aeiou]/i;
    function fixArticles(){
        slangifiedSpans.forEach(span=>{
            const prev=span.previousSibling;
            if(prev&&prev.nodeType===3){ // text node
                const txt=prev.textContent;
                const m=txt.match(/(\b)(a|an)(\s+)$/i);
                if(m){ const correct=vowelRegex.test(span.textContent)?'an':'a'; if(m[2].toLowerCase()!==correct){ prev.textContent=txt.slice(0,m.index)+m[1]+correct+m[3]; } }
            }
        });
    }

    /* ─────────── SLANGIFY ─────────── */
    function slangify(txt){ if(skipParent(txt.parentNode)) return; const val=txt.nodeValue; if(!dictRegex.test(val)) return; dictRegex.lastIndex=0;
        const frag=document.createDocumentFragment(); let last=0,m; while((m=dictRegex.exec(val))){ if(m.index>last) frag.append(val.slice(last,m.index));
            const span=document.createElement('span'); const orig=m[0]; span.className='gzs-word'; span.title=orig;
            span.textContent=slangDict[orig.toLowerCase()]; span.style.cursor='help'; if(highlightEnabled) span.style.backgroundColor='#fffd6b';
            frag.append(span); slangifiedSpans.push(span); slangCount++; last=m.index+orig.length; }
        if(last<val.length) frag.append(val.slice(last)); if(txt.parentNode) txt.parentNode.replaceChild(frag,txt); }

    /* ─────────── PROCESSOR ─────────── */
    function process(){ if(isProcessing||!queue.length) return; isProcessing=true;
        const step=()=>{ let c=0; while(queue.length&&c<20){ const n=queue.shift(); if(n&&n.parentNode) slangify(n); c++; }
            if(queue.length) requestAnimationFrame(step); else { isProcessing=false; fixArticles(); updateCount(slangCount); if(highlightEnabled) navWrap.style.display='flex'; } };
        requestAnimationFrame(step); }

    function pushNodes(root){ const w=document.createTreeWalker(root,NodeFilter.SHOW_TEXT,null,false); let n; while((n=w.nextNode())) if(!skipParent(n.parentNode)) queue.push(n); }
    function scan(){ pushNodes(document.body); process(); }

    /* ─────────── RESET / REVERT ─────────── */
    function revert(){ document.querySelectorAll('span.gzs-word').forEach(span=> span.replaceWith(span.title)); }
    function reset(){ revert(); slangifiedSpans.length=0; queue.length=0; slangCount=0; navIndex=-1; updateCount(0); }

